// basic js program using if else 

// This program checks if a number is positive, negative, or zero

let number = 5;

if (number > 0) 
    {
    console.log("The number is positive.");
} 
else if (number < 0) 
    {
    console.log("The number is negative.");
} 
else 
{
    console.log("The number is zero.");
}
